  static class Solution {
    public int minArray(int[] numbers) {
      return method1(numbers);
    }

    /**
     *
     * 分而治之
     *
     * 执行用时： 0 ms , 在所有 Java 提交中击败了 100.00% 的用户
     *
     * <p>内存消耗： 38.3 MB , 在所有 Java 提交中击败了 41.81% 的用户
     *
     * @param numbers
     * @return
     */
    private int method1(int[] numbers) {
      return min(numbers, 0, numbers.length - 1);
    }

    private int min(int[] numbers, int l, int r) {
      if (l + 1 >= r) {
        return Math.min(numbers[l], numbers[r]);
      }
      if (numbers[l] < numbers[r]) {
        return numbers[l];
      }
      int mid = l + ((r - l) >> 1);
      return Math.min(min(numbers, l, mid - 1), min(numbers, mid, r));
    }
  }
